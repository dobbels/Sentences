    public static (SentenceStem SentenceStem, int Week) GetRandomSentence()
    {
        var sentences = GetSentences();

        var random = new Random();
        var weekNumber = random.Next(1, sentences.Count + 1);
        var weekSentences = sentences[weekNumber];
        var randomSentence = weekSentences[random.Next(weekSentences.Length)];

        return (randomSentence, weekNumber);
    }
